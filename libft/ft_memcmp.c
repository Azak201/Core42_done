#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char	*a;
	char	*b;

	a = (char *)s1;
	b = (char *)s2;
	while (n-- && (*a == *b) && a && b)
	{
		a++;
		b++;
	}
	return (*a - *b);
}
